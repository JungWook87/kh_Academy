-- 3번 !!
SELECT PROFESSOR_NAME 교수이름 ,  
	EXTRACT (YEAR FROM SYSDATE) - (TO_NUMBER(SUBSTR(PROFESSOR_SSN, 1, 2))+1900) 나이
FROM TB_PROFESSOR tp
WHERE SUBSTR(PROFESSOR_SSN, 8, 1) = '1' 
ORDER BY 나이;


SELECT PROFESSOR_NAME 교수이름 ,
 TRUNC(MONTHS_BETWEEN(SYSDATE, TO_DATE(SUBSTR(PROFESSOR_SSN, 1, INSTR(PROFESSOR_SSN, '-')-1), 'RRMMDD'))/12) 나이	
FROM TB_PROFESSOR tp
WHERE SUBSTR(PROFESSOR_SSN, 8, 1) = '1' 
ORDER BY 나이;


